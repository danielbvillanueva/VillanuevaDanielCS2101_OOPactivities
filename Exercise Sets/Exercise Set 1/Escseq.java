
public class Escseq {
    public static void main(String[] args) {
        for(int i = 1; i <= 5; i++) {
            int n = i * 9;

            if(n % 2 == 0){
                System.out.println("\t" + n);
            }
            else{
                System.out.println(n);
                }
            }
        }

        
}